package com.it.prakash;

public class Demo {
	
	
	
	
	public static void main(String[] args) {
		
		int a[] = new int[5]; //Declaration and instantiation
		
		a[0]=5;  //initialization
		a[1]=10;
		a[2]=15;
		a[3]=25;
		a[4]=35;
		//traversing array
		for( int i=0; i<a.length; i++ ) //length
		
		System.out.println(a[i]);
	}

}



